import 'dart:math';

enum AiMood {
  calm,
  neutral,
  low,
}

class AiVibeService {
  AiVibeService._();

  static final AiVibeService instance = AiVibeService._();

  final _random = Random();

  Future<String> generateAffirmation(AiMood mood) async {
    // Мок-генерация: имитация запроса к LLM.
    await Future<void>.delayed(const Duration(milliseconds: 600));

    final variants = switch (mood) {
      AiMood.calm => <String>[
          'Сделай один глубокий вдох и почувствуй, как тело мягко расслабляется. Сегодня ты можешь идти в своём темпе.',
          'Ты уже достаточно сделал(а). Позволь себе замедлиться и просто побыть в тишине пару минут.',
          'Твоё спокойствие — твоя сила. Мир может спешить, но ты выбираешь мягкое и осознанное движение.',
        ],
      AiMood.neutral => <String>[
          'Сегодня хороший день, чтобы сделать один маленький шаг к тому, о чём ты давно думаешь.',
          'Остановись на пару вдохов и вспомни: ты можешь выбирать, куда направить своё внимание.',
          'Добавь немного заботы о себе в расписание дня — даже 5 минут тишины могут изменить настрой.',
        ],
      AiMood.low => <String>[
          'Тебе не нужно «исправлять» себя. Просто будь рядом с собой и дыши чуть глубже, чем обычно.',
          'Даже если сейчас тяжело, это состояние не навсегда. Сделай мягкий вдох и позволь себе немного поддержки.',
          'Ты уже справлялся(ась) со сложными днями раньше. Сегодня можно быть мягче к себе, чем обычно.',
        ],
    };

    return variants[_random.nextInt(variants.length)];
  }
}

